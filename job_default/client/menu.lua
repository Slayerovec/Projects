-- basic
function jobMenu()
    WarMenu.CreateMenu("jobmenu", "Job Menu", "Možnosti")
    WarMenu.OpenMenu("jobmenu")
    WarMenu.SetMenuY("jobmenu", 0.35)

    while true do
        if WarMenu.IsMenuOpened("jobmenu") then
            if WarMenu.Button("Nějaká pičovina") then
                WarMenu.CloseMenu()
            elseif WarMenu.Button("Nějaká pičovina") then
                WarMenu.CloseMenu()
            elseif WarMenu.Button("Nějaká pičovina") then
                WarMenu.CloseMenu()
            elseif WarMenu.Button("Nějaká pičovina") then
                WarMenu.CloseMenu()
            elseif WarMenu.Button("Nějaká pičovina") then
                WarMenu.CloseMenu()
            elseif WarMenu.Button("Nějaká pičovina") then
                WarMenu.CloseMenu()
            elseif WarMenu.Button("Nějaká pičovina") then
                WarMenu.CloseMenu()
            elseif WarMenu.Button("Nějaká pičovina") then
                WarMenu.CloseMenu()
            end

            WarMenu.Display()
        else
            WarMenu.CloseMenu()
            break
        end

        Citizen.Wait(0)
    end
end

